public class CalculadoraSimples {
	public static void main(String[] args) {
		int a = 10;
		int b = 2;

		System.out.println("O resultado da soma de " + a + " + " + b + " = " + (a+b));
		System.out.println("O resultado da subtração de " + a + " - " + b + " = " + (a-b));
		System.out.println("O resultado da multiplicação de " + a + " * " + b + " = " + (a*b));
		System.out.println("O resultado da divisão de " + a + " / " + b + " = " + (a/b));
		System.out.println("O resto da divisão de " + a + " % " + b + " = " + (a%b));
	}
} 